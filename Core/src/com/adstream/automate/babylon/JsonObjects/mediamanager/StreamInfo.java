public class StreamInfo
{
    private String template;

    private String fileSize;

    private String firstFrameTimeCode;

    private String fileType;

    private String audioCodec;

    private String jobId;

    private String length;

    private String videoCodec;

    private String sourceFileName;

    private String templateNotes;

    public String getTemplate ()
    {
        return template;
    }

    public void setTemplate (String template)
    {
        this.template = template;
    }

    public String getFileSize ()
    {
        return fileSize;
    }

    public void setFileSize (String fileSize)
    {
        this.fileSize = fileSize;
    }

    public String getFirstFrameTimeCode ()
    {
        return firstFrameTimeCode;
    }

    public void setFirstFrameTimeCode (String firstFrameTimeCode)
    {
        this.firstFrameTimeCode = firstFrameTimeCode;
    }

    public String getFileType ()
    {
        return fileType;
    }

    public void setFileType (String fileType)
    {
        this.fileType = fileType;
    }

    public String getAudioCodec ()
    {
        return audioCodec;
    }

    public void setAudioCodec (String audioCodec)
    {
        this.audioCodec = audioCodec;
    }

    public String getJobId ()
    {
        return jobId;
    }

    public void setJobId (String jobId)
    {
        this.jobId = jobId;
    }

    public String getLength ()
    {
        return length;
    }

    public void setLength (String length)
    {
        this.length = length;
    }

    public String getVideoCodec ()
    {
        return videoCodec;
    }

    public void setVideoCodec (String videoCodec)
    {
        this.videoCodec = videoCodec;
    }

    public String getSourceFileName ()
    {
        return sourceFileName;
    }

    public void setSourceFileName (String sourceFileName)
    {
        this.sourceFileName = sourceFileName;
    }

    public String getTemplateNotes ()
    {
        return templateNotes;
    }

    public void setTemplateNotes (String templateNotes)
    {
        this.templateNotes = templateNotes;
    }

 }
