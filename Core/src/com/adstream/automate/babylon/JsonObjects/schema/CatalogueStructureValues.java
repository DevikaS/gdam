package com.adstream.automate.babylon.JsonObjects.schema;

public class CatalogueStructureValues {
    private String CustomCharacters;

    private String FieldName;

    private String FieldChoice;

    public String getCustomCharacters ()
    {
        return CustomCharacters;
    }

    public void setCustomCharacters (String CustomCharacters)
    {
        this.CustomCharacters = CustomCharacters;
    }

    public String getFieldName ()
    {
        return FieldName;
    }

    public void setFieldName (String FieldName)
    {
        this.FieldName = FieldName;
    }

    public String getFieldChoice ()
    {
        return FieldChoice;
    }

    public void setFieldChoice (String FieldChoice)
    {
        this.FieldChoice = FieldChoice;
    }


}
